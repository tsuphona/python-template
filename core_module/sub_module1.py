# -*- coding: utf-8 -*-


class CoreClass1:
    """Simple arithmetic class.

    This class provide methods to do simple arithmetics.

    Attributes:

    Args:
    """

    @staticmethod
    def add(a: int, b: int) -> int:
        """Add two integers.

        Add two integers together and return the sum.

        Args:
          a (int): The first integer.
          b (int): The second integer.

        Returns:
          a + b (int): The sum of a and b.

        Raise:
        """
        return a + b

    @staticmethod
    def subtract(a: int, b: int) -> int:
        """Subtract two integers.

        Subtract two integers and return the difference.

        Args:
          a (int): The first integer.
          b (int): The second integer.

        Returns:
          a - b (int): The difference of a and b.

        Raise:
        """
        return a - b
