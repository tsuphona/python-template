# -*- coding: utf-8 -*-


class CoreClass2:
    """Complex arithmetic class.

    This class provide methods to do complex arithmetics.

    Attributes:

    Args:
    """

    @staticmethod
    def multiply(a: float, b: float) -> float:
        """Multiply two floats.

        Multiply two integers together and return the product.

        Args:
          a (float): The first float.
          b (float): The second float.

        Returns:
          a * b (float): The product of a and b.

        Raise:
        """
        return a * b

    @staticmethod
    def divide(a: float, b: float) -> float:
        """Divide two floats.

        Divide two floats and return the quotient.

        Args:
          a (float): The dividend.
          b (float): The divisor.

        Returns:
          a / b (float): The quotient of a and b.

        Raise:
        """
        return a / b
